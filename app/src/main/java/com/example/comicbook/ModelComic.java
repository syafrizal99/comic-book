package com.example.comicbook;

public class ModelComic {

    private String titleComic;
    private int imageComic;
    private int imageFullComic;

    public int getImageFullComic() {
        return imageFullComic;
    }

    public void setImageFullComic(int imageFullComic) {
        this.imageFullComic = imageFullComic;
    }

    public String getTitleComic() {
        return titleComic;
    }

    public void setTitleComic(String titleComic) {
        this.titleComic = titleComic;
    }

    public int getImageComic() {
        return imageComic;
    }

    public void setImageComic(int imageComic) {
        this.imageComic = imageComic;
    }


}
